    private void AnalyseBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnalyseBActionPerformed
        // TODO add your handling code here:
        EmployeeSalary.Function f1=new EmployeeSalary.Function();
            rs=null;
            rs=f1.find(ENameTF.getText());
            
            int ID=0;
            String Name="";
            String Post="";
            String Salary="";
            

        try{
            
            if(rs.next())
            {
                    ID=Integer.parseInt(rs.getString("empId"));
                    Name=rs.getString("empName");
                    Post=rs.getString("empPOST");
                    Salary=rs.getString("empSalary");
                    
                    EIdTF.setText(String.valueOf(ID));
                    EName2TF.setText(Name);
                    EPostTF.setText(Post);
                    ESalaryTF.setText(Salary);
            }   
            else
            {
                System.out.println("NO DATA");
            }
        }
        catch(Exception e){
            System.out.println("Exception:"+e.getMessage());
        }
    }//GEN-LAST:event_AnalyseBActionPerformed

