    public class Function
    {
        public ResultSet find(String s)
        {
            try{
            st = con.createStatement();
            ps=con.prepareStatement("select empId,empName,empPOST,empSalary from employee where empName = ?");
            ps.setString(1,s);
            rs = ps.executeQuery();
            }
            catch(Exception e)
            {
                System.out.println(e.getMessage());
            }
           return rs;
        }
    }
    
